    private void ClickEditButon(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ClickEditButon
        // TODO add your handling code here:
        String userIdStr = IdField.getText().trim();
        if (userIdStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Id field don't exists!", "Error !!!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int userId = Integer.parseInt(userIdStr);

        String userName = userNameField.getText().trim();
        String gender = jComboBox1.getSelectedItem().toString();
        int phone = Integer.parseInt(phoneField.getText().trim());
        String image = ImageField.getText().trim();
        String email = emailField.getText().trim();
        String password = new String(passwordField.getPassword());
        String option = jComboBox2.getSelectedItem().toString();
        String hashedPassword = null;

        try {
            hashedPassword = PasswordUtils.hashPassword(password);
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi mã hóa mật khẩu!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        UsersModel updatedUser = new UsersModel();
        updatedUser.setId(userId); 
        updatedUser.setUserName(userName);
        updatedUser.setGender(gender);
        updatedUser.setPhone(phone);
        updatedUser.setImage(image);
        updatedUser.setEmail(email);
        updatedUser.setPassWord(hashedPassword); 
        updatedUser.setOption(option);

        UserDAO userDAO = new UserDAO();
        int result = userDAO.Update(updatedUser);

        if (result > 0) {
            JFrame parentFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
            
            parentFrame.dispose();
            CustomDialog dialog = new CustomDialog((JFrame) SwingUtilities.getWindowAncestor(this), "Success", "User editor successfully!");
            
        } else {
            JOptionPane.showMessageDialog(this, "Error edit", "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_ClickEditButon

