    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // Lấy thông tin chỉnh sửa từ các trường dữ liệu trên form
        String newImage = urlImage.getText();
        String newCategory = (String) comboBoxCategory.getSelectedItem();
        String newProduct = txtProduct.getText();
        String newSize = (String) comboBoxSize.getSelectedItem();
        String newPrice = txtPrice.getText();
        
        // Thực hiện cập nhật trong cơ sở dữ liệu
        ProductDao productDao = new ProductDao();
        if (productDao.updateProduct(ID, newImage, newCategory, newProduct, newSize, newPrice)) {
            // Cập nhật thành công, thông báo và cập nhật bảng
            JOptionPane.showMessageDialog(this, "Cập nhật thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            parentForm.updateProductTable();
            this.dispose(); // Đóng cửa sổ chỉnh sửa
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật không thành công!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

