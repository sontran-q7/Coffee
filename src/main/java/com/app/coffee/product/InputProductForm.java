    private void btnChooseImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseImageActionPerformed
        // Đường dẫn tới thư mục chứa các hình ảnh
        String imageDirectoryPath = "C:\\Users\\Acer\\OneDrive\\Pictures\\bh3rd";
        // Tạo một đối tượng File để tham chiếu đến thư mục chứa hình ảnh
        File imageDirectory = new File(imageDirectoryPath);
        // Kiểm tra xem thư mục có tồn tại không và có phải là thư mục không
        if (!imageDirectory.exists() || !imageDirectory.isDirectory()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy thư mục chứa hình ảnh.");
            return;
        }
        // Lấy danh sách các file hình ảnh từ thư mục
        File[] imageFiles = imageDirectory.listFiles();
        // Kiểm tra xem có file hình ảnh nào trong thư mục không
        if (imageFiles == null || imageFiles.length == 0) {
            JOptionPane.showMessageDialog(this, "Thư mục không chứa hình ảnh nào.");
            return;
        }
        // Tạo một mảng String để lưu trữ tên của các file hình ảnh
        String[] imageNames = new String[imageFiles.length];
        for (int i = 0; i < imageFiles.length; i++) {
            imageNames[i] = imageFiles[i].getName();
        }
        // Hiển thị danh sách các hình ảnh cho người dùng chọn
        String selectedImageName = (String) JOptionPane.showInputDialog(
                this,
                "Chọn một hình ảnh:",
                "Chọn hình ảnh",
                JOptionPane.PLAIN_MESSAGE,
                null,
                imageNames,
                imageNames[0]);
        // Nếu người dùng đã chọn một hình ảnh
        if (selectedImageName != null && !selectedImageName.isEmpty()) {
            // Tạo đường dẫn tuyệt đối của hình ảnh đã chọn
            String selectedImagePath = imageDirectoryPath + File.separator + selectedImageName;

            // Hiển thị hình ảnh xem trước
            ImageIcon icon = new ImageIcon(selectedImagePath);
            Image scaledImage = icon.getImage().getScaledInstance(300, 300, Image.SCALE_DEFAULT);
            icon = new ImageIcon(scaledImage);
            JLabel imagePreviewLabel = new JLabel();
            imagePreviewLabel.setIcon(icon);
            JOptionPane.showMessageDialog(this, imagePreviewLabel, "Preview Image", JOptionPane.PLAIN_MESSAGE);
            // Lưu đường dẫn hình ảnh vào urlImage
            urlImage.setText(selectedImagePath);
        }

    }//GEN-LAST:event_btnChooseImageActionPerformed


